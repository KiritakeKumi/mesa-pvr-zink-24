import argparse
import os
import sys
import textwrap

import codegen

h = None
c = None


def generate_register_configs(set):
    register_types = {
        'FLEX': 'flex_regs',
        'NOA': 'mux_regs',
        'OA': 'b_counter_regs',
    }

    c("void %s_%s_add_registers(struct intel_perf_config *perf, struct intel_perf_query_info *query)" %
      (set.gen.chipset, set.underscore_name))
    c("{")
    c.indent(4)

    # allocate memory
    total_n_registers = {}
    register_configs = set.findall('register_config')
    for register_config in register_configs:
        t = register_types[register_config.get('type')]
        if t not in total_n_registers:
            total_n_registers[t] = len(register_config.findall('register'))
        else:
            total_n_registers[t] += len(register_config.findall('register'))

    # fill in register/values
    register_configs = set.findall('register_config')
    for register_config in register_configs:
        t = register_types[register_config.get('type')]

        availability = register_config.get('availability')
        if availability:
            set.gen.output_availability(set, availability, register_config.get('type') + ' register config')
            c.indent(4)

        c("static const struct intel_perf_query_register_prog %s[] = {" % t)
        c.indent(4)
        for register in register_config.findall('register'):
            c("{ .reg = %s, .val = %s }," % (register.get('address'),
                                             register.get('value')))
        c.outdent(4)
        c("};")
        c("query->config.%s = %s;" % (t, t))
        c("query->config.n_%s = %u;" % (t, total_n_registers[t]))


        if availability:
            c.outdent(4)
            c("}")
            c("\n")

    c.outdent(4)
    c("}")


