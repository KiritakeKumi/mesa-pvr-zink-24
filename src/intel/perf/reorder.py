def print_err(*args):
    sys.stderr.write(' '.join(map(str,args)) + '\n')

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", help="Input", required=True)

    args = parser.parse_args()

    root = et.parse(args.input)

    # print('    \'{0}\': {{'.format(args.input))
    # for set in root.findall('.//set'):
    #     print('        \'{0}\': ['.format(set.attrib['symbol_name']))
    #     for counter in set.findall('.//counter'):
    #         print('            \'{0}\','.format(counter.attrib['symbol_name']))
    #     print('        ],')
    # print('    },')

    # return

    print('<?xml version="1.0"?>')
    metrics = root.getroot()
    print('<metrics version="{0}" merge_md5="">'.format(metrics.attrib['version']))

    for set in root.findall('.//set'):
        print('  <set name="{0}"'.format(set.attrib['name']))
        for a in ('chipset', 'symbol_name', 'underscore_name', 'mdapi_supported_apis', 'hw_config_guid'):
            print('       {0}="{1}"'.format(a, set.attrib[a]))
        print('       >')

        counters = {}
        for counter in set.findall('.//counter'):
            counters[counter.attrib['symbol_name']] = counter

        def print_counter(counter):
            print('    <counter name="{0}"'.format(counter.attrib['name']))
            for a in ('symbol_name', 'underscore_name', 'description',
                      'low_watermark', 'high_watermark',
                      'data_type', 'max_equation', 'units', 'semantic_type', 'equation',
                      'availability', 'mdapi_group', 'mdapi_usage_flags', 'mdapi_supported_apis',
                      'mdapi_hw_unit_type'):
                if a in counter.attrib:
                    print('             {0}="{1}"'.format(a, counter.attrib[a]))
            print('             />')


        for counter_name in orders[args.input][set.attrib['symbol_name']]:
            if not counter_name in counters:
                continue
            print_counter(counters[counter_name])
        for counter_name in counters.keys():
            if counter_name in orders[args.input][set.attrib['symbol_name']]:
                continue
            print_counter(counters[counter_name])

        for regs in set.findall('.//register_config'):
            if len(regs.attrib) < 2:
                print('    <register_config type="{0}">'.format(regs.attrib['type']))
            else:
                print('    <register_config type="{0}"'.format(regs.attrib['type']))
                if 'availability' in regs.attrib:
                    print('                     availability="{0}"'.format(regs.attrib['availability'].replace('&', '&amp;')))
                if 'priority' in regs.attrib:
                    print('                     priority="{0}"'.format(regs.attrib['priority']))
                print('                     >')

            for r in regs.findall('.//register'):
                print('        <register type="{0}" address="{1}" value="{2}" />'.format(r.attrib['type'],
                                                                                         r.attrib['address'],
                                                                                         r.attrib['value']))
            print('    </register_config>')

        print('  </set>')
        print('')

    print('</metrics>')


if __name__ == '__main__':
    main()
